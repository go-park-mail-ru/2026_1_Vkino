INSERT INTO episode (
    movie_id, description, season_number, episode_number, title,
    duration_seconds, picture_file_key, video_file_key
)
SELECT
    m.id,
    seed.description,
    seed.season_number,
    seed.episode_number,
    seed.title,
    greatest(
        1200,
        m.duration_seconds + CASE (seed.season_number + seed.episode_number) % 3
            WHEN 0 THEN -120
            WHEN 1 THEN 0
            ELSE 120
        END
    ),
    m.picture_file_key,
    format(
        '%s_s%se%s.mp4',
        replace(m.picture_file_key, '.webp', ''),
        lpad(seed.season_number::text, 2, '0'),
        lpad(seed.episode_number::text, 2, '0')
    )
FROM tmp_series_episode_seed seed
JOIN movie m ON m.title = seed.movie_title
ON CONFLICT (movie_id, season_number, episode_number) DO NOTHING;

