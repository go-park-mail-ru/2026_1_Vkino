        m.title,
        m.description
    ),
    1,
    1,
    m.title,
    m.duration_seconds,
    m.picture_file_key,
    format(
        '%s.mp4',
        replace(m.picture_file_key, '.webp', '')
    )
FROM movie m
WHERE m.content_type = 'film'
ON CONFLICT (movie_id, season_number, episode_number) DO NOTHING;

