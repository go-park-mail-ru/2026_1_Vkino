create table if not exists user_interaction_review_reaction (
    id bigint generated by default as identity primary key,

    review_id bigint not null
        references user_interaction (id)
            on update cascade
            on delete cascade,

    user_id bigint not null
        references users (id)
            on update cascade
            on delete cascade,

    reaction text not null
        constraint user_interaction_review_reaction_value_check
            check (reaction in ('like', 'dislike')),

    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),

    constraint user_interaction_review_reaction_unique unique (review_id, user_id)
);

create index if not exists user_interaction_review_reaction_review_idx
    on user_interaction_review_reaction (review_id);

