insert into movie_external_rating (movie_id, source, value, scale)
select
    m.id,
    seeded.source,
    seeded.value,
    10.00
from rating_seed rs
join movie m on m.title = rs.movie_title
cross join lateral (
    values
        ('IMDb'::text, rs.imdb_value::numeric(4, 2)),
        ('Кинопоиск'::text, rs.kinopoisk_value::numeric(4, 2))
) as seeded(source, value)
on conflict (movie_id, source) do update
set
    value = excluded.value,
    scale = excluded.scale,
    updated_at = now();

